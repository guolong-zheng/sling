void g_slist_free(Node * l)
  /*D_requires sll^(x) */
  /*D_ensures emp */
{

  Node * x = l;
  while(x != NULL)
  /*D_invariant list^(next) */
  {
    Node * next = x->next;
    free(x);
    x = next;
  }
}

